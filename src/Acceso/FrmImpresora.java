    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        btnnuevoregi.setEnabled(true);
        noeditable(false, false, false, false, false, false);
        int resp;

        resp = JOptionPane.showConfirmDialog(null, "¿Desea grabar el registro?", "Pregunta", 0);
        if (resp == 0) {
            try {
                conn = Acceso.Enlace(conn);
                st = Acceso.sta(st);
                rs = Acceso.EnlImpresora(rs);
                //variables
                String comando, cod, marimp = "", idmarimp, tipimpre, descripcion, modelo, fallos;
                //variables
                cod = txtidimpresora.getText();
                idmarimp = (String) cbomarcaimpresora.getSelectedItem();
                rs10 = Acceso.obtenerCodMarcImp(rs10, idmarimp);
                if (rs10.next()) {
                    marimp = rs10.getString(1);
                }
                tipimpre = (String) cbotipoimpresion.getSelectedItem();
                descripcion = txtdescrip.getText();
                modelo = txtmodelos.getText();
                fallos = txtfallos.getText();

                //COMANDOS
                comando = "INSERT INTO impresora VALUES('" + cod + "','" + marimp + "','" + tipimpre + "','" + descripcion + "',"
                        + "'" + modelo + "','" + fallos + "')";
                st.executeUpdate(comando);

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Demasiados Caracteres, resume mejor");
                return;
                // JOptionPane.showMessageDialog(null, "Error "+e.toString());
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        int resp;
        noeditable(false, false, false, false, false, false);
        resp = JOptionPane.showConfirmDialog(null, "¿Desea modificar el registro?", "Pregunta", 0);
        if (resp == 0) {
            try {
                conn = Acceso.Enlace(conn);
                st = Acceso.sta(st);
                rs = Acceso.EnlImpresora(rs);

                String comando, cod, marimp = "", idmarimp, tipimpre, descripcion, modelo, fallos;
                //variables
                cod = txtidimpresora.getText();
                idmarimp = (String) cbomarcaimpresora.getSelectedItem();
                rs10 = Acceso.obtenerCodMarcImp(rs10, idmarimp);
                if (rs10.next()) {
                    marimp = rs10.getString(1);
                }
                tipimpre = (String) cbotipoimpresion.getSelectedItem();
                descripcion = txtdescrip.getText();
                modelo = txtmodelos.getText();
                fallos = txtfallos.getText();

                comando = "UPDATE impresora SET id_marca_impresora='" + marimp + "', tipo_impresion='" + tipimpre + "',descripcion='" + descripcion + "'"
                        + ",modelo='" + modelo + "',fallos='" + fallos + "'";
                comando = comando + " WHERE id_impresora=" + cod;
                st.executeUpdate(comando);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "¡Hey eso no puedes hacer!, verifica no cambiar el codigo de la Impresora");
                return;
            }
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String Buscarcliente;//BUSCAMOS LOS CLIENTES EN LA BASE DE DATOS SEGUN SUS CODIGOS
        if (btnnuevoregi.isEnabled()) {
            try {
                conn = Acceso.Enlace(conn);
                rs = Acceso.ObtenerVistaImpresora(rs);
                Buscarcliente = txtidimpresora.getText();
                boolean encuentra = false;
                while (rs.next()) {
                    if (Buscarcliente.equals(rs.getString(1))) {
                        cbomarcaimpresora.setSelectedItem((String) rs.getString(2));
                        cbotipoimpresion.setSelectedItem((String) rs.getString(3));
                        txtdescrip.setText((String) rs.getString(4));
                        txtmodelos.setText((String) rs.getString(5));
                        txtfallos.setText((String) rs.getString(6));
                        editable(true, true, true, true, true, true);
                        encuentra = true;
                        break;
                    }
                }
                if (encuentra == false)//SI NO SE ENCUENTRA EL CODIGO DEBERAN SALDRA EL MENSAJE DE NO EXISTEN DATOS
                {
                    JOptionPane.showMessageDialog(null, "No se hallan datos, corrija el codigo");
                    txtidimpresora.setText("No Existe");

                    editable(true, true, true, true, true, true);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR BD" + e.getMessage());
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

